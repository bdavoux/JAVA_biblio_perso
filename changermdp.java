    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ancienmdp = jTextField1.getText();
        String nouveaumdp = jTextField2.getText();
        String confirmation = jTextField3.getText();
        
        String mdp = maBiblio.uco.obtenirMdp();
        
        if (!ancienmdp.equals(mdp)){
            JOptionPane.showMessageDialog(null, "Mot de passe incorrect.", "Erreur", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            if (nouveaumdp.equals(confirmation)){
                JOptionPane.showMessageDialog(null, "Nouveau mot de passe.", "Succ√®s", JOptionPane.INFORMATION_MESSAGE);
                maBiblio.uco.motDePasse = nouveaumdp;
                maBiblio.sauvegarderUtilisateurs("utilisateurs");
                
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

